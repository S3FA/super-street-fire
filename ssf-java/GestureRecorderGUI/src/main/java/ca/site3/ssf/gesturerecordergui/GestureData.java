package ca.site3.ssf.gesturerecordergui;

// An object that represents the gesture data
public class GestureData {
	
	// Left hand gyroscope tuple
	public String gyroLeft;
	
	// Left hand magnetometer tuple
	public String magLeft;
	
	// Left hand accelerometer tuple
	public String accLeft;
	
	// Right hand gyroscope tuple
	public String gyroRight;
	
	// Right hand magnetometer tuple
	public String magRight;
	
	// Right hand accelerometer tuple
	public String accRight;
	
	// The gesture name
	public String GestureName;
	
	// The time of the data capture
	public String Time;
}
